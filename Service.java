package m10d20.project2;

/**
 * 服务接口 - 项目的核心抽象层
 * <p>
 * 这个接口定义了所有文本处理服务必须遵循的标准契约。
 * 通过统一的接口，实现了"接口+匿名内部类"设计模式的基础。
 * 所有服务实现（无论是命令行还是图形界面版本）都通过此接口进行统一调用。
 * </p>
 */
public interface Service {
    /**
     * 执行具体的文本处理服务
     * <p>
     * 这是服务的核心方法，接收输入文本并返回处理后的结果。
     * 不同的服务实现会在此方法中提供不同的文本处理逻辑。
     * </p>
     * @param input 用户输入的文本数据
     * @return 服务处理后的文本结果
     */
    String execute(String input);
    
    /**
     * 获取服务的名称标识
     * <p>
     * 提供服务的唯一标识名称，用于在程序中识别和展示不同的服务。
     * </p>
     * @return 服务的名称
     */
    String getName();
}